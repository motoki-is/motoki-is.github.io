    <p class="explanation">家具メーカである綾野製作所様のブランド、XENO NOIRを紹介するビジュアルブック・カタログを担当した。

「おもい」を持った確かな物作りと使いやすさへのこだわりが特徴の綾野製作所。

その中のブランドの一つ、XENO NOIR（ゼノノワール）はシステムユニットの延長線にありながら最高峰を目指す商品開発のもと他製品との流用性や、ディテール・パーツ・仕上に極限までこだわった家具ブランド。

家具にこだわりを持ったお客様へ紹介するツールとして、持って帰りたくなる佇まい、インテリアの一助になる佇まいをコンセプトに製作した。

ビジュアルブックとタブロイド型のカタログの2つで構成。

ビジュアルブックはイメージ優先に振り切り、写真のみで伝える冊子に。表紙はローボードの天板のテクスチャを用いてXENO NOIRらしい佇まいを目指した。

カタログは機能性、スペックをイラストを用いながら、しっかりとまとめた。表紙、裏表紙には商品のカラーバリエーションを大きめに配置。裏面にあるポスターは、XENOシリーズの特徴であるアジャスターをモチーフに天板のカラーと綾野製作所の赤色でシンプルに構成した。
</p>

