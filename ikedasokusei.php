    <p class="explanation">
岡山市北区本町で八百屋さんを営んでいる
池田促成青果ラボさまのステッカーとポスターデザインを担当。

ドリンクやショッパーなど、様々なツールに貼ることを想定し
野菜・果物のおもしろさを色・形で実感できるステッカーを制作した。

オクラ・ピーマン・ズッキーニ・ゴーヤ・びわ・水菜の根元。
それぞれの断面から意外性のあるおもしろい形を取り出し、ハンコにした。
赤・緑・黄・紫の4色を使い、ステッカーに押して使用する。
色・形の組み合わせから、様々なものを連想してもらいたい。

酵素玄米の販売にあわせて店頭に飾るポスターは、
もっちりと美味しそうに見える写真と「酵素玄米」の文字を大きく入れることで、
道行く人や車で通りがかった人の目にとまるようにした。

酵素玄米は、代謝をあげてくれたり、お通じを良くしてくれたり、
他にもたくさんからだに良い働きをしてくれる。
酵素玄米のことを知らない人にも興味を持ってもらえるよう、
それをシンプルなコピーで表現した。
</p>

