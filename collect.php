    <p class="explanation">Japan blue が展開するファブリックブランド「コレクト」のリブランディングを担当。

桃太郎ジーンズなどのブランドを展開されているjapanblue。
この会社のルーツでありテキスタイルの企画販売事業部であるコレクト。
メーカーとして、そしてアパレルブランドを支える生地のプロフェショナルとして、日本、瀬戸内、児島という場所から一流のものづくりを牽引している。

[collect] ロゴのデザインには、一見普通に見えるそれぞれのフォントは少しづつ変化をまとっていて、旧式力織機が生む均質化されていない個性のあるもの作りという思いを重ねている。
また、コレクトの根幹をなす象徴的な商品でもあるセルビッチデニムの特徴であるセルビッチ（耳）をモチーフに組み込み、japannblueの中で唯一BtoBをフィールド とする業態から、 それぞれの顧客の要望に耳を傾ける、常にその耳で新しい情報を集め続ける、 という姿勢を象徴的に表現している。

また、ロゴのリニューアルに合わせて先ずはwebsiteも新しく開設。
<a class="corporatesite" href="https://collect-denim.jp/">コレクト ウェブサイト</a>

コレクトのバックボーンである、瀬戸内にある児島の空気感を伝えることが まずは大事な要素であると考えた。
24時間毎の瀬戸内海の見える児島の風景を撮りため、 アクセスする時間と同時刻の画像が表示されるように組み込んだ。 アクセスする時間でサイトの表紙が変化するという仕掛け。
また、スクロールに合わせて見出しを縦方向に動かし、横方向に配置したテキストと重ねて、織を連想させた。

    
