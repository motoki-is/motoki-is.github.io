    <p class="explanation">兵岡山県農林水産部 鳥獣害対策室主催のイベント『大おかやまジビエ博』『ジビエカレースタンプラリー』のデザインを担当した。

ジビエとは脂肪が少なく栄養価も高い、天然の食材。 一方で野生鳥獣による農作物被害や森林被害で厄介者として処分されている現状。 少しでも多くの人にジビエを知ってもらい、ジビエの普及推進につなげるために、大おかやまジビエ博で様々な催しが行われた。

キービジュアルは「ジビエを知ってもらう」ために「ジビエ」の文字で構成。 ジビエの一文字ずつに捕獲、加工、食を手のイラストを使って表し、 野生・山をイメージした緑色と、シカやイノシシなどのジビエ肉から抽出したピンク色のパターンを、チラシ・レトルトパッケージ・のぼり・ポップ・テレビCMなどに展開。 Webサイトは画面両側の草むらを各コンテンツが駆け抜けるようなエフェクトを用いて構成した。 各会場構成・デザインも担当。タペストリーは装飾を兼ねながら、ジビエのことを説明する媒体として活用した。

ジビエカレーのスタンプラリーでは、緑とピンクのキーカラーを使ったチラシ・専用サイトをデザイン。 チラシはカレー皿をモチーフにしたイラストを作成し、楕円形に仕上げた。 また、ノベルティの鹿角スプーン製作のディレクションを行なった。
</p>

