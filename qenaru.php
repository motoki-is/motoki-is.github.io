    <p class="explanation">岡山県北部、蒜山高原というロケーションに新しくオープンした、サンマルクグループの新業態でサスティナブルをテーマに展開する「トラットリアケナル」 の店舗トータルでのブランディングデザインのプロジェクト。

都市部でない山間部の観光地でのあらたなチャレンジでもあるこのプロジェクトをすすめていくにあたって、避暑地として観光客がたくさん訪れるハイシーズンよりも、そうでない閑散期といわれるシーズンのお店のあり方について着目しながらデザインコンセプトを固めていった。
店名の由来でもある昔使われていたこの場所の地名「ケナル」をきっかけに、「ハレとケ」の概念から、ハイシーズンに訪れる観光客ではなく、そこで生活されてるケ（日常）の場となるような場所を目指すことで、その地域に根ざした地元の方が日常使いできるような店として愛されるのではないかと考えた。

内装には地元の材木屋さんを回りながら見つけてきた木材のその表情を生かしつつ、出来るだけ最小限の加工でその製造工程も省略化しテーブル、ベンチ、スツール、造作材を製作。
コンセプトである「ケ」をモチーフに同サイズで製作した木タイルを数種類のパターン化で展開し、さまざまな表情を表現した。

グラフィックツールは素材を大事にしたメニュー開発によって生まれてくるメニューを邪魔しないようにできるだけシンプルにそれらを引き立たせるデザインを目指し、ウェブサイトでは、ケナルの考え方や取り組みをシンプルに、ダイレクトに感じてもらえるようなデザインを目指した。
ユニフォームにはジャパンブルー社に協力いただきながらバナナの繊維を使用した生地を使ったエプロンを製作し、それをコーヒーの出がらしで染めたナチュラルなブラウンの色味で落ち着いた雰囲気を演出することを狙った。
また、ケジャーナルというフリーペーパーを発刊し、ケナルと蒜山、ケナルとその関係してくださる方々を定期的に紹介、発信していきたいと考えている。


