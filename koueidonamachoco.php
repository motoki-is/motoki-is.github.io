    <p class="explanation">廣榮堂武田さまのブランディングプロジェクトの一環で、
『生チョコもち』シリーズのパッケージをリニューアルした。

発売から20年経ち定番商品となった生チョコもち、
コロンとした可愛らしいかたちそのものをモチーフに。
これから先も長く愛されるシンプルで普遍的なデザインを意識して制作した。

生チョコもちは、生チョコをミルク味の求肥で包み、更にココアパウダーをまぶしており、
小さいながらも、生チョコ・求肥・ココアと丁寧に味が織り重なっているところを
緩やかなトーンでわずかにグラデーションをかけることで表現。
一見無機質でシンプルな図案でありながら、奥行感、美味しそうな印象を作った。

また、生チョコもちの部分にのみ、ズラして光沢ニスをかけることで
人の手を介して作るアナログ感、立体感を出した。
生チョコが滑らかで柔らかいゆえ、一つ一つ形が違うことから、
かたちをあえて不揃いにして並べた。 </p>

