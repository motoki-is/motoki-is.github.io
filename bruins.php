    <p class="explanation">岡山の15歳以下のアイスホッケーチーム、OKAYAMA BRUINS（岡山ブルーインズ）のロゴのリニューアルとユニフォームデザインを担当した。

"BRUIN"とは「クマくん」という意味。西日本に生息するツキノワグマをモチーフに、以前から使われているチームカラーの青と黄色を使ってロゴをデザインした。

クマの顔のシンボルマークは、氷のイメージで輪郭をカクカクとさせた。アイスホッケーの国際ルールでは22人がベンチ入りできることから、22人が一体となって1匹のクマになるように、22本の直線からできている。さらに鼻の形はパックのシルエットになっている。

「BRUINS」の文字には、1文字に1本ずつスティックが使われており、6文字のスティックで氷上の6人の選手を表現した。スティックの間を縫うように入っているツキノワグマの月は、6人がゴールに向かってパックをつなげる様子、スピード感、そしてVICTORYの“V”の意味がこめられている。</p>
