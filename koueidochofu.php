    <p class="explanation">兵廣榮堂武田さまの銘菓『調布』のパッケージデザインを担当。
廣榮堂武田さまの調布は、皮が一枚一枚手焼きで作られており、肌理がとても細かく、綺麗な卵色。

この皮の表情を生かしたパッケージをデザインした。
”調布”という言葉が古の貢物で良質な布をあらわしていることに因んで、
たゆたう布をモチーフにした模様が、パッケージに包まれることで浮かんでくる仕掛けにした。
皮の色味が映えるようパッケージは白を基調にデザイン。

