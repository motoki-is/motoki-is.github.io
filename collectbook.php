    <p class="explanation">Japan blueが展開するデニムテキスタイルメーカー「コレクト」の新たな2つの生地のネーミングを担当。 また、コレクトの考えを体現する2つの生地のストーリーを伝えるコンセプトブックを制作した。

1つめの生地は、超長綿とよばれる世界最高級のコットンを使用したもので、滑らかで光沢のある繊維が特長。 通常デニムに使用されることのない貴重なコットンを使用している。まさに最高品質のデニムであり、 デニムの最上位を表すべく『 D.est（デスト）』とネーミング。 また繊維長の数値そのものがD.est を示すものであり、副題にはその数値を配した。

もう1つは、灌漑システムやGPS機能など最新科学を用いたオーガニック農法で生産されたコットンを使用したデニム。 デニムの中のデニムと言えるような、本来の力強さを追求した生地として 『Thed（セッド）』と名付けた。 遠い未来にヴィンテージと呼ばれ、継承され続けるデニムを目指したこの生地のメッセージを副題にした。

この2つの生地のラインは、これからのものづくりにおけるサスティナブルであることの重要性、 そのために信頼性の高いアメリカの希少なオーガニックコットンを使用していること、 そしてそれを確実に実現するためにトレサビリティを徹底していることが共通項。 コンセプトブックはその共通項を軸にして、2つのラインを1冊まとめた。全体にその証となる資料や写真を、コラージュを用いながら表現。 生産されるストーリーを構成する、水、益虫、農場システム、サーティフィケイトなどのキーワードを、テキストではなくビジュアルで使った。

一流技術と環境保護への高い意識を持つパートナーたちが、紡績・染色・整理加工に携わっていることを明記。 コレクトが、これらの素材とパートナーを選び、そして織を行うことで、最高品質のセルヴィッチデニムを実現している。

なお、コンセプトブックには、デニム端材を粉砕して練り込んだ再生紙を使用。 厚みのある紙質を生かし箔押しで凹凸を出した。 左表紙には金箔でD.est、右表紙には銀箔でThedのタイトルを入れた。

同素材でタグも製作した。</p>

