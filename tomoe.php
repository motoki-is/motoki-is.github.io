    <p class="explanation">後楽ホテル様が運営する欧風レストラン巴様の ロゴマーク、各種ツールデザイン、内装のリニューアルを担当。

農 Agriculture 、創 Originality 、食 Gastronomy
３つの要素を一つにする『巴（ともえ）』と名付けられたレストラン。
後楽ホテル様の本体は農機具メーカーの「みのる産業」。
農を考える会社、岡山のホテル、欧風レストラン、その背景をヒントに取り組んだ。

農家の方が作った作物をフレンチとして、お客様へお届けする。
ロゴマークには、その流れや食の拡がりを「巴」の形をベースに曲線で表した。
T…Tomoe   M…Minoru   E…岡山の山
それぞれに背景の単語を当てはめ、間のoは小文字で表記。
曲線はレストランが面する西川の流れも表現している。

ロゴの曲線を元にビュッフェポップ、メニュー表、チラシ、看板など、各種ツールへ展開させた。

元は和食店であった木目調の内装をグレーに一新。
パーティションや個室の扉など既存の和風建具を改造して活用、新しい空間に合うよう作り変えた。
エントランス正面には引き戸の開閉で朝食とランチの切り替えを行える壁面収納を制作。
従来よりも作業効率を上げながら、用途に応じた空間の使い分けができる仕掛けにした。
</p>

